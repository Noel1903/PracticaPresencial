class operaciones:

	def __init__(self,valor1,valor2):
		self.valor1=valor1
		self.valor2=valor2

	def sumavalor(valor1,valor2):
		return self.valor1+self.valor2

	def restavalor(valor1,valor2):
		return self.valor1-self.valor2


	def multiplicarvalor(valor1,valor2):
		return self.valor1*self.valor2


	def divisionvalor(valor1,valor2):
		return self.valor1/self.valor2

	def dump(self):
		return{
			'valor1':valor1,
			'valor2':valor2

		}

